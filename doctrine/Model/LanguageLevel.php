<?php

namespace Model;

/**
 * LanguageLevel
 */
class LanguageLevel
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $level;

    /**
     * @var string
     */
    private $level_name;

    /**
     * @var string
     */
    private $level_group;

    /**
     * @var string
     */
    private $level_group_name;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set level
     *
     * @param string $level
     *
     * @return LanguageLevel
     */
    public function setLevel($level)
    {
        $this->level = $level;

        return $this;
    }

    /**
     * Get level
     *
     * @return string
     */
    public function getLevel()
    {
        return $this->level;
    }

    /**
     * Set levelName
     *
     * @param string $levelName
     *
     * @return LanguageLevel
     */
    public function setLevelName($levelName)
    {
        $this->level_name = $levelName;

        return $this;
    }

    /**
     * Get levelName
     *
     * @return string
     */
    public function getLevelName()
    {
        return $this->level_name;
    }

    /**
     * Set levelGroup
     *
     * @param string $levelGroup
     *
     * @return LanguageLevel
     */
    public function setLevelGroup($levelGroup)
    {
        $this->level_group = $levelGroup;

        return $this;
    }

    /**
     * Get levelGroup
     *
     * @return string
     */
    public function getLevelGroup()
    {
        return $this->level_group;
    }

    /**
     * Set levelGroupName
     *
     * @param string $levelGroupName
     *
     * @return LanguageLevel
     */
    public function setLevelGroupName($levelGroupName)
    {
        $this->level_group_name = $levelGroupName;

        return $this;
    }

    /**
     * Get levelGroupName
     *
     * @return string
     */
    public function getLevelGroupName()
    {
        return $this->level_group_name;
    }
}

